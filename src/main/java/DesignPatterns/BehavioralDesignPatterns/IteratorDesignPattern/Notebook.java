package DesignPatterns.BehavioralDesignPatterns.IteratorDesignPattern;
public class Notebook {

  private String name = "";

  public Notebook(String name) {
    super();
    this.name = name;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}